function p = gps3d(a,b,c,d,ra,rb,rc,rd)
A = [2*a(1,1) - 2*b(1,1), 2*a(2,1) - 2*b(2,1), 2*a(3,1) - 2*b(3,1); 
    2*b(1,1) - 2*c(1,1), 2*b(2,1) - 2*c(2,1), 2*b(3,1) - 2*c(3,1);
    2*c(1,1) - 2*d(1,1), 2*c(2,1) - 2*d(2,1), 2*c(3,1) - 2*d(3,1);];
b = [(-ra^2 + rb^2 + a(1,1)^2 + a(2,1)^2 + a(3,1)^2 - b(1,1)^2 - b(2,1)^2 - b(3,1)^2);
    (-rb^2 + rc^2 + b(1,1)^2 + b(2,1)^2 + b(3,1)^2 - c(1,1)^2 - c(2,1)^2 - c(3,1)^2);
    (-rc^2 + rd^2 + c(1,1)^2 + c(2,1)^2 + c(3,1)^2 - d(1,1)^2 - d(2,1)^2 - d(3,1)^2)];
p = solve(A,b);